#Calcular e mostrar área de um trapézio. Sabe-se que: A = (base maior + base menor)* altura)/2
altura = float (input("Insira a altura: "))
base_maior = float  (input("Insira a base maior"))
base_menor = float (input("Insira a base menor"))
trapezio = (base_maior + base_menor)* altura/2

print("A área do trapezio é de:" ,trapezio)

