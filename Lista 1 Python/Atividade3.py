#Quantidade de vendas e quanto deve guardar

paes= float (input("Insira a quantidade de paes vendidos"))

broas= float (input("Insira a quantidade de broas vendidas"))

total = (paes*0.12) + (broas*1.50)

print ("A quantidade total das vendas é: ",total)
print ("A quantidade que deve guardar é: ",total*0.10)
